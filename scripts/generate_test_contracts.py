def _wrap_text(draw: ImageDraw.ImageDraw, text: str, font: ImageFont.FreeTypeFont, max_width: int) -> list[str]:
    lines: list[str] = []
    current = ""
    for ch in text:
        trial = current + ch
        if draw.textlength(trial, font=font) <= max_width:
            current = trial
        else:
            if current:
                lines.append(current)
            current = ch
    if current:
        lines.append(current)
    return lines


def create_docx(contract: dict) -> None:
    doc = Document()
    normal = doc.styles["Normal"]
    normal.font.name = "Microsoft YaHei"
    normal.font.size = Pt(11)

    title = doc.add_paragraph()
    title.alignment = WD_PARAGRAPH_ALIGNMENT.CENTER
    run = title.add_run(contract["title"])
    run.bold = True
    run.font.size = Pt(18)

    sub = doc.add_paragraph()
    sub.alignment = WD_PARAGRAPH_ALIGNMENT.CENTER
    sub_run = sub.add_run(contract["subtitle"])
    sub_run.font.size = Pt(10.5)

    note = doc.add_paragraph()
    note_run = note.add_run("说明：本合同为测试样本，所有主体、地址、账号、金额等信息均为虚构，仅用于系统测试。")
    note_run.italic = True
    note_run.font.size = Pt(9.5)

    for heading, paragraphs in contract["sections"]:
        p = doc.add_paragraph()
        r = p.add_run(heading)
        r.bold = True
        r.font.size = Pt(12.5)
        for item in paragraphs:
            pp = doc.add_paragraph()
            rr = pp.add_run(item)
            rr.font.size = Pt(11)

    doc.save(BASE_DIR / contract["filename"])


def create_scan_pdf(contract: dict) -> None:
    pages: list[Image.Image] = []
    page = Image.new("RGB", (PAGE_W, PAGE_H), (247, 243, 235))
    draw = ImageDraw.Draw(page)

    def add_header(img_draw: ImageDraw.ImageDraw, y0: int) -> int:
        img_draw.text((MARGIN_X, y0), contract["title"], font=TITLE_FONT, fill=(35, 40, 54))
        img_draw.text((MARGIN_X, y0 + 56), contract["subtitle"], font=SUB_FONT, fill=(110, 104, 96))
        img_draw.line((MARGIN_X, y0 + 95, PAGE_W - MARGIN_X, y0 + 95), fill=(210, 198, 180), width=2)
        return y0 + 125

    def finalize(img: Image.Image) -> Image.Image:
        noise = Image.new("RGB", (PAGE_W, PAGE_H), (0, 0, 0))
        noise_draw = ImageDraw.Draw(noise)
        for _ in range(160):
            x = random.randint(0, PAGE_W - 1)
            y = random.randint(0, PAGE_H - 1)
            tone = random.randint(220, 245)
            noise_draw.point((x, y), fill=(tone, tone, tone))
        return Image.blend(img, noise, 0.03).filter(ImageFilter.GaussianBlur(0.2))

    y = add_header(draw, TOP_Y)

    for idx, block in enumerate(contract["blocks"]):
        lines = _wrap_text(draw, block, BODY_FONT, PAGE_W - 2 * MARGIN_X)
        needed = len(lines) * (BODY_FONT.size + LINE_GAP) + PARA_GAP
        if y + needed > PAGE_H - BOTTOM_MARGIN:
            pages.append(finalize(page))
            page = Image.new("RGB", (PAGE_W, PAGE_H), (247, 243, 235))
            draw = ImageDraw.Draw(page)
            y = add_header(draw, TOP_Y)
        for line in lines:
            draw.text((MARGIN_X + random.randint(-1, 1), y), line, font=BODY_FONT, fill=(42, 46, 58))
            y += BODY_FONT.size + LINE_GAP
        y += PARA_GAP
        if idx in {0, 3, 7}:
            draw.line((MARGIN_X, y - 8, PAGE_W - MARGIN_X, y - 8), fill=(232, 224, 210), width=1)
            y += 6

    draw.text((MARGIN_X, PAGE_H - 80), "测试扫描件 OCR 使用", font=SMALL_FONT, fill=(145, 138, 130))
    pages.append(finalize(page))

    first, *rest = pages
    first.save(BASE_DIR / contract["filename"], "PDF", resolution=150.0, save_all=True, append_images=rest)


def validate_docx(filename: str) -> None:
    doc = Document(BASE_DIR / filename)
    text = "\n".join(p.text for p in doc.paragraphs)
    if "合同" not in text or "甲方" not in text:
        raise RuntimeError(f"{filename} 内容校验失败")


def main() -> None:
    for contract in DOCX_CONTRACTS:
        create_docx(contract)
        validate_docx(contract["filename"])

    for contract in PDF_CONTRACTS:
        create_scan_pdf(contract)

    print(f"已生成 {len(DOCX_CONTRACTS)} 份 DOCX 和 {len(PDF_CONTRACTS)} 份 PDF 到 {BASE_DIR}")


if __name__ == "__main__":
    main()
